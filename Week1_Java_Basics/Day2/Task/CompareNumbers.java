public class CompareNumbers {
    public static void main(String[] args) {
        int a = 25;
        int b = 42;

        if (a > b){
            System.out.println(a + " is larger than " + b);
        } else if (a < b){
            System.out.println(b + " is larger than " + a);
        } else {
            System.out.println("Both numbers are equal");
        }
    }
}
